/**
 * BRAND INTEGRITY AUDIT
 * Removes all mentions of AI, automation, and ensures natural, human feel
 * Ensures website is optimized for premium subscription conversion
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, '../..');

// Patterns that MUST be removed or replaced
const FORBIDDEN_PATTERNS = [
  // AI mentions
  /\bAI\b/gi,
  /artificial intelligence/gi,
  /powered by AI/gi,
  /AI-generated/gi,
  /machine learning/gi,
  /neural network/gi,
  /algorithm/gi,
  /generated by algorithm/gi,
  /computer generated/gi,

  // Automation mentions
  /\bautomatic\b/gi,
  /\bautomated\b/gi,
  /automation/gi,
  /auto-generated/gi,
  /automatically generated/gi,

  // Tech jargon that kills mystical feel
  /computational/gi,
  /data-driven/gi,
  /statistical/gi,
  /computational analysis/gi,
  /digital analysis/gi,

  // Suspicious phrases
  /may not be accurate/gi,
  /for entertainment purposes/gi,
  /not guaranteed/gi,
  /disclaimer/gi,
  /not real/gi,
  /fictional/gi,
];

// Words/phrases that reveal AI origin
const SUSPICIOUS_PHRASES = [
  'based on your input',
  'here is your reading',
  'generated reading',
  'calculated result',
  'your compatibility score',
  'your analysis',
  'according to',
  'based on the data',
  'your profile',
  'technical analysis',
  'digital interpretation',
];

// Good replacements for natural language
const REPLACEMENTS = {
  'AI': 'intuitive',
  'artificial intelligence': 'mystical guidance',
  'powered by AI': 'crafted with care',
  'AI-generated': 'personally interpreted',
  'machine learning': 'ancestral wisdom',
  'algorithm': 'divination system',
  'automated': 'personalized',
  'automatic': 'flowing naturally',
  'automation': 'divine timing',
  'generated by algorithm': 'revealed through',
  'computer generated': 'spiritually channeled',
  'based on your input': 'aligned with your energy',
  'calculated result': 'divine insight',
  'statistical': 'time-honored',
  'data-driven': 'intuition-guided',
  'computational': 'profound',
};

/**
 * Audit file for AI mentions and suspicious phrases
 */
function auditFile(filePath, showDetails = false) {
  try {
    const content = fs.readFileSync(filePath, 'utf8');
    const issues = [];

    // Check forbidden patterns
    for (const pattern of FORBIDDEN_PATTERNS) {
      const matches = content.match(pattern);
      if (matches) {
        issues.push({
          type: 'forbidden_pattern',
          pattern: pattern.toString(),
          count: matches.length,
          matches: matches.slice(0, 3) // First 3 matches
        });
      }
    }

    // Check suspicious phrases
    for (const phrase of SUSPICIOUS_PHRASES) {
      if (content.toLowerCase().includes(phrase.toLowerCase())) {
        const regex = new RegExp(phrase, 'gi');
        const matches = content.match(regex);
        issues.push({
          type: 'suspicious_phrase',
          phrase: phrase,
          count: matches.length
        });
      }
    }

    return { issues, hasIssues: issues.length > 0 };
  } catch (error) {
    return { issues: [], error: error.message, hasIssues: false };
  }
}

/**
 * Fix file by removing/replacing AI mentions
 */
function fixFile(filePath) {
  try {
    let content = fs.readFileSync(filePath, 'utf8');
    let changesCount = 0;

    // Apply replacements
    for (const [bad, good] of Object.entries(REPLACEMENTS)) {
      const regex = new RegExp(`\\b${bad}\\b`, 'gi');
      if (regex.test(content)) {
        content = content.replace(regex, good);
        changesCount++;
      }
    }

    // Remove forbidden patterns
    for (const pattern of FORBIDDEN_PATTERNS) {
      const matches = content.match(pattern);
      if (matches) {
        content = content.replace(pattern, '');
        changesCount += matches.length;
      }
    }

    if (changesCount > 0) {
      fs.writeFileSync(filePath, content, 'utf8');
      return { fixed: true, changesCount };
    }

    return { fixed: false, changesCount: 0 };
  } catch (error) {
    return { fixed: false, error: error.message };
  }
}

/**
 * Main audit function
 */
export async function auditBrandIntegrity() {
  console.log('\n🔍 BRAND INTEGRITY AUDIT');
  console.log('   Scanning for AI mentions & unnatural language...\n');

  const htmlFiles = [];
  const jsFiles = [];
  const problematicFiles = [];

  // Find all HTML files
  function walkDir(dir) {
    try {
      const files = fs.readdirSync(dir);
      for (const file of files) {
        const filePath = path.join(dir, file);
        const stat = fs.statSync(filePath);

        if (stat.isDirectory()) {
          // Skip certain directories
          if (!['node_modules', '.git', 'dist', 'build'].includes(file)) {
            walkDir(filePath);
          }
        } else if (file.endsWith('.html')) {
          htmlFiles.push(filePath);
        } else if (file.endsWith('.js')) {
          jsFiles.push(filePath);
        }
      }
    } catch (error) {
      // Skip inaccessible directories
    }
  }

  walkDir(rootDir);

  // Audit all files
  const allFiles = [...htmlFiles, ...jsFiles];
  console.log(`📋 Scanning ${allFiles.length} files...\n`);

  for (const filePath of allFiles) {
    const result = auditFile(filePath);

    if (result.hasIssues) {
      const relativePath = path.relative(rootDir, filePath);
      problematicFiles.push({
        file: relativePath,
        issues: result.issues
      });

      console.log(`⚠️  ${relativePath}`);
      for (const issue of result.issues) {
        if (issue.type === 'forbidden_pattern') {
          console.log(`   ❌ Forbidden: ${issue.pattern} (${issue.count}x)`);
        } else {
          console.log(`   ⚠️  Suspicious: "${issue.phrase}" (${issue.count}x)`);
        }
      }
    }
  }

  console.log(`\n📊 AUDIT RESULTS:`);
  console.log(`   Total files scanned: ${allFiles.length}`);
  console.log(`   Files with issues: ${problematicFiles.length}`);

  if (problematicFiles.length === 0) {
    console.log('\n✅ PERFECT! No AI mentions or suspicious language found!');
  } else {
    console.log(`\n⚠️  Found issues in ${problematicFiles.length} files`);
    console.log('\n🔧 Run: node server/scripts/fix-brand-integrity.js');
  }

  return {
    status: problematicFiles.length === 0 ? 'clean' : 'issues_found',
    scanned_files: allFiles.length,
    problematic_files: problematicFiles.length,
    issues_detail: problematicFiles
  };
}

// Run if called directly
if (import.meta.url === `file://${process.argv[1]}`) {
  const result = await auditBrandIntegrity();
  console.log('\n' + JSON.stringify(result, null, 2));
}

export { auditFile, fixFile };
