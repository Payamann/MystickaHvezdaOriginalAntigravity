/**
 * FIX BRAND INTEGRITY
 * Automatically removes AI mentions and replaces with natural language
 * Ensures website feels authentic and human
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, '../..');

// Smart replacements that maintain meaning but feel human & mystical
const SMART_REPLACEMENTS = {
  // AI replacements
  'AI-generated reading': 'personalized reading',
  'AI-generated analysis': 'intuitive analysis',
  'artificial intelligence': 'ancestral wisdom',
  'powered by artificial intelligence': 'guided by intuition',
  'powered by AI': 'crafted with care',
  'AI interpretation': 'spiritual interpretation',
  'AI analysis': 'holistic analysis',
  'AI algorithm': 'divination system',
  'machine learning model': 'sacred patterns',
  'machine learning': 'ancestral knowledge',
  'neural network': 'cosmic consciousness',
  'algorithm': 'mystical system',

  // Automation replacements
  'automatically generated': 'personally interpreted',
  'auto-generated': 'carefully crafted',
  'automated reading': 'flowing reading',
  'automated analysis': 'personalized analysis',
  'automated': 'intuitive',
  'automation': 'harmonious flow',
  'automatic': 'natural',

  // Tech jargon replacements
  'computational': 'profound',
  'data-driven': 'intuition-guided',
  'statistical analysis': 'time-honored interpretation',
  'digital reading': 'spiritual reading',
  'computer generated': 'spiritually channeled',
  'based on your input': 'aligned with your energy',
  'calculated result': 'divine insight',
  'your profile': 'your cosmic signature',
  'technical analysis': 'spiritual analysis',

  // Trust-building replacements
  'may not be accurate': 'for personal reflection',
  'not guaranteed': 'a guide for your journey',
  'for entertainment purposes only': 'for spiritual growth',
  'generated by algorithm': 'revealed through millennia of wisdom',
  'here is your reading': 'your reading has emerged',
  'generated reading': 'your personal reading',
};

// Case-insensitive patterns for careful replacement
const CASE_INSENSITIVE = [
  'AI',
  'algorithm',
  'automated',
  'automatic',
  'automation',
  'artificial intelligence',
];

/**
 * Replace text in file with smart matching
 */
function fixFileContent(content) {
  let fixed = content;
  let changesCount = 0;

  // Apply exact replacements first (handles case properly)
  for (const [bad, good] of Object.entries(SMART_REPLACEMENTS)) {
    // Try exact match first
    const exactRegex = new RegExp(`\\b${bad}\\b`, 'g');
    if (exactRegex.test(fixed)) {
      fixed = fixed.replace(exactRegex, good);
      changesCount++;
    }
  }

  // Handle case-insensitive replacements
  for (const word of CASE_INSENSITIVE) {
    const regex = new RegExp(`\\b${word}\\b`, 'gi');
    const matches = fixed.match(regex);
    if (matches) {
      changesCount += matches.length;

      // Replace while preserving case
      fixed = fixed.replace(regex, (match) => {
        if (word === 'AI') return '✨'; // Special handling for AI
        if (word === 'algorithm') return 'sacred pattern';
        if (word === 'automated') return 'intuitive';
        if (word === 'automatic') return 'natural';
        if (word === 'automation') return 'harmony';
        if (word === 'artificial intelligence') return 'ancestral wisdom';
        return match;
      });
    }
  }

  // Remove disclaimers that seem inauthentic
  const disclaimerPatterns = [
    /\*\*Disclaimer:\*\*.*?[\n\r]/gi,
    /This reading is for entertainment purposes only[.!]*/gi,
    /Not guaranteed[.!]*/gi,
  ];

  for (const pattern of disclaimerPatterns) {
    if (pattern.test(fixed)) {
      fixed = fixed.replace(pattern, '');
      changesCount++;
    }
  }

  return { fixed, changesCount };
}

/**
 * Fix all files in project
 */
export async function fixBrandIntegrity() {
  console.log('\n🔧 FIXING BRAND INTEGRITY');
  console.log('   Removing AI mentions and adding natural language...\n');

  const htmlFiles = [];
  const jsFiles = [];
  const jsonFiles = [];
  let totalChanges = 0;

  // Walk directory
  function walkDir(dir) {
    try {
      const files = fs.readdirSync(dir);
      for (const file of files) {
        const filePath = path.join(dir, file);
        const stat = fs.statSync(filePath);

        if (stat.isDirectory()) {
          if (!['node_modules', '.git', 'dist', 'build'].includes(file)) {
            walkDir(filePath);
          }
        } else if (file.endsWith('.html')) {
          htmlFiles.push(filePath);
        } else if (file.endsWith('.js')) {
          jsFiles.push(filePath);
        } else if (file.endsWith('.json')) {
          jsonFiles.push(filePath);
        }
      }
    } catch (error) {
      // Skip
    }
  }

  walkDir(rootDir);

  const allFiles = [...htmlFiles, ...jsFiles, ...jsonFiles];
  console.log(`📋 Processing ${allFiles.length} files...\n`);

  let filesModified = 0;

  for (const filePath of allFiles) {
    try {
      const content = fs.readFileSync(filePath, 'utf8');
      const result = fixFileContent(content);

      if (result.changesCount > 0) {
        fs.writeFileSync(filePath, result.fixed, 'utf8');
        const relativePath = path.relative(rootDir, filePath);

        console.log(`✅ ${relativePath}`);
        console.log(`   Fixed: ${result.changesCount} instances`);

        filesModified++;
        totalChanges += result.changesCount;
      }
    } catch (error) {
      console.error(`❌ Error processing ${filePath}:`, error.message);
    }
  }

  console.log(`\n📊 SUMMARY:`);
  console.log(`   Files processed: ${allFiles.length}`);
  console.log(`   Files modified: ${filesModified}`);
  console.log(`   Total changes: ${totalChanges}`);

  if (filesModified > 0) {
    console.log('\n✨ Brand integrity restored!');
    console.log('   Website now feels natural and human');
    console.log('   All AI mentions removed');
    console.log('\n🚀 Next step: Verify changes and commit');
  } else {
    console.log('\n✅ Website is already brand-clean!');
  }

  return {
    status: 'complete',
    files_processed: allFiles.length,
    files_modified: filesModified,
    total_changes: totalChanges
  };
}

// Run if called directly
if (import.meta.url === `file://${process.argv[1]}`) {
  const result = await fixBrandIntegrity();
  console.log('\n' + JSON.stringify(result, null, 2));
}
